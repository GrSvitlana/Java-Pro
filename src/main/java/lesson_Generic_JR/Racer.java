package lesson_Generic_JR;

public abstract class Racer {
    private String name;

    public Racer(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
